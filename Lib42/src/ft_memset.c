#include "../include/libft.h"

void	*ft_memset(void *b, int c, int len)
{
	int				i;
	unsigned char	*str;

	str = (unsigned char *)b;
	i = 0;
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	b = str;
	return (b);
}
